package variables;

public class Estudiante {

	public static void main(String[] args) {
		// Declaración de variables de diferentes tipos de datos primitivos
        int edad = 20; // Número entero
        double altura = 1.75; // Número decimal
        char inicial = 'J'; // Caracter
        boolean estudiante = true; // Valor booleano (verdadero o falso)

        // Imprimir los valores de las variables
        System.out.println("Edad: " + edad);
        System.out.println("Altura: " + altura);
        System.out.println("Inicial: " + inicial);
        System.out.println("¿Es estudiante? " + estudiante);

	}

}
 