package variables;

public class Operaciones {

	public static void main(String[] args) {
		 // Declaración de variables
        int num1 = 10, num2 = 5;
        double num3 = 4.5;
        char letra = 'A';

        // Operaciones aritméticas con enteros y decimales
        int suma = num1 + num2; 
        double division = num1 / num3; 

        // Imprimir resultados
        System.out.println("Suma de num1 y num2: " + suma);
        System.out.println("División de num1 entre num3: " + division);
        System.out.println("Letra almacenada en la variable 'letra': " + letra);

	}

}
